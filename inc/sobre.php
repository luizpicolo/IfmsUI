    <h1>Principal</h1>
    <p>
        A IFMS User Interface, ou IFMS-UI, é uma interface gráfica em desenvolvimento que visará atender projetos de iniciação científica desenvolvidos por alunos/pesquisadores do Instituto Federal de Mato Grosso do Sul - Campus de Nova Andradina. Tem como foco central o desenvolvimento de uma interface minimalista e amigável, a qual priorizará a facilidade do uso e os princípios básicos de usabilidade.
    </p>
